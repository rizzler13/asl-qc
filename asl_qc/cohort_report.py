# Correlation matrix
# ──────────────────────────────────────────────

def _correlation_matrix(subjects):
    if not subjects or len(subjects) < 5:
        return ""

    try:
        plt = _setup_mpl()
    except Exception:
        return ""

    keys = ["snr", "spatial_cov", "negative_fraction",
            "dvars_spike_fraction", "skewness", "qei", "tsnr", "mean_fd"]
    labels = ["SNR", "CoV", "Neg%", "DVARS", "Skew", "QEI", "tSNR", "FD"]

    data = []
    for s in subjects:
        row = []
        for k in keys:
            v = s.get(k)
            row.append(v if v is not None and _is_finite(v) else np.nan)
        data.append(row)

    arr = np.array(data)

    # drop columns that are all NaN
    valid_cols = ~np.all(np.isnan(arr), axis=0)
    arr = arr[:, valid_cols]
    labels_filtered = [l for l, v in zip(labels, valid_cols) if v]

    if arr.shape[1] < 2:
        return ""

    try:
        n_cols = arr.shape[1]
        corr = np.zeros((n_cols, n_cols))
        for i in range(n_cols):
            for j in range(n_cols):
                valid = ~(np.isnan(arr[:, i]) | np.isnan(arr[:, j]))
                if valid.sum() > 2:
                    corr[i, j] = np.corrcoef(arr[valid, i], arr[valid, j])[0, 1]
                else:
                    corr[i, j] = np.nan

        fig, ax = plt.subplots(figsize=(6, 5))
        im = ax.imshow(corr, cmap="coolwarm", vmin=-1, vmax=1, aspect="equal")

        ax.set_xticks(range(len(labels_filtered)))
        ax.set_yticks(range(len(labels_filtered)))
        ax.set_xticklabels(labels_filtered, fontsize=9, rotation=45, ha="right")
        ax.set_yticklabels(labels_filtered, fontsize=9)

        for i in range(len(labels_filtered)):
            for j in range(len(labels_filtered)):
                if math.isfinite(corr[i, j]):
                    color = "white" if abs(corr[i, j]) > 0.6 else "black"
                    ax.text(j, i, f"{corr[i,j]:.2f}", ha="center", va="center",
                            fontsize=8, color=color)

        fig.colorbar(im, ax=ax, shrink=0.8, label="Pearson r")
        fig.tight_layout()

        b64 = _fig_to_b64(fig)
        return f"""
<h2>Inter-Metric Correlations</h2>
<div class="plot-cell" style="max-width:600px">
<img src="data:image/png;base64,{b64}" alt="Correlation matrix">
</div>
"""
    except Exception:
        return ""


