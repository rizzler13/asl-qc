def _anomaly_section(subjects, if_results):
    if not subjects or len(subjects) < 3:
        return ""

    if if_results is None:
        return """
<h2>Anomaly Detection</h2>
<p style="color:#888">
Isolation forest was not run — fewer than 10 subjects
or scikit-learn not installed.
</p>
"""

    n_anom = if_results.get("n_anomalies", 0)

    # build interactive SVG scatter plot
    svg = _anomaly_svg(subjects)
    if not svg:
        return ""

    return f"""
<h2>Anomaly Detection</h2>
<p style="font-size:12px; color:#666">
Isolation forest flagged {n_anom} subject(s) as anomalous.
Hover over points to see subject IDs.
</p>
<div class="anomaly-container" id="anomaly-wrap">
<div class="tooltip" id="anomaly-tip"></div>
{svg}
</div>
{_anomaly_hover_js()}
"""


def _anomaly_svg(subjects):
    """Build an inline SVG scatter plot with data attributes for hover."""
    # collect data
    points = []
    for s in subjects:
        anom = s.get("anomaly_score")
        snr = s.get("snr")
        if anom is None or snr is None:
            continue
        if not _is_finite(anom) or not _is_finite(snr):
            continue
        points.append({
            "x": anom,
            "y": snr,
            "label": s.get("subject_id", "?"),
            "status": s.get("status", "PASS"),
            "is_anomaly": s.get("if_label", 1) == -1,
        })

    if len(points) < 2:
        return ""

    # coordinate mapping
    xs = [p["x"] for p in points]
    ys = [p["y"] for p in points]
    x_min, x_max = min(xs), max(xs)
    y_min, y_max = min(ys), max(ys)

    # add 5% padding
    x_pad = max((x_max - x_min) * 0.08, 0.1)
    y_pad = max((y_max - y_min) * 0.08, 0.5)
    x_min -= x_pad; x_max += x_pad
    y_min -= y_pad; y_max += y_pad

    w, h = 560, 360
    margin = {"l": 55, "r": 20, "t": 15, "b": 45}
    pw = w - margin["l"] - margin["r"]
    ph = h - margin["t"] - margin["b"]

    def sx(v):
        return margin["l"] + (v - x_min) / (x_max - x_min) * pw

    def sy(v):
        return margin["t"] + (1 - (v - y_min) / (y_max - y_min)) * ph

    lines = [f'<svg width="{w}" height="{h}" xmlns="http://www.w3.org/2000/svg" '
             f'style="background:#fff;font-family:Inter,system-ui,sans-serif">']

    # axes
    ax_y = margin["t"] + ph
    lines.append(f'<line x1="{margin["l"]}" y1="{margin["t"]}" '
                 f'x2="{margin["l"]}" y2="{ax_y}" stroke="#ccc" stroke-width="1"/>')
    lines.append(f'<line x1="{margin["l"]}" y1="{ax_y}" '
                 f'x2="{margin["l"]+pw}" y2="{ax_y}" stroke="#ccc" stroke-width="1"/>')

    # axis labels
    lines.append(f'<text x="{margin["l"]+pw//2}" y="{h-5}" '
                 f'text-anchor="middle" font-size="11" fill="#666">'
                 f'Anomaly score</text>')
    lines.append(f'<text x="14" y="{margin["t"]+ph//2}" '
                 f'text-anchor="middle" font-size="11" fill="#666" '
                 f'transform="rotate(-90,14,{margin["t"]+ph//2})">'
                 f'SNR</text>')

    # tick marks
    n_xticks = 5
    for i in range(n_xticks + 1):
        v = x_min + i * (x_max - x_min) / n_xticks
        px = sx(v)
        lines.append(f'<line x1="{px:.1f}" y1="{ax_y}" x2="{px:.1f}" '
                     f'y2="{ax_y+4}" stroke="#ccc" stroke-width="1"/>')
        lines.append(f'<text x="{px:.1f}" y="{ax_y+16}" text-anchor="middle" '
                     f'font-size="9" fill="#999">{v:.2f}</text>')

    n_yticks = 5
    for i in range(n_yticks + 1):
        v = y_min + i * (y_max - y_min) / n_yticks
        py = sy(v)
        lines.append(f'<line x1="{margin["l"]-4}" y1="{py:.1f}" '
                     f'x2="{margin["l"]}" y2="{py:.1f}" stroke="#ccc"/>')
        lines.append(f'<text x="{margin["l"]-8}" y="{py+3:.1f}" '
                     f'text-anchor="end" font-size="9" fill="#999">'
                     f'{v:.1f}</text>')
        # grid line
        lines.append(f'<line x1="{margin["l"]}" y1="{py:.1f}" '
                     f'x2="{margin["l"]+pw}" y2="{py:.1f}" '
                     f'stroke="#f0f0f0" stroke-width="0.5"/>')

    # data points
    for p in points:
        px, py = sx(p["x"]), sy(p["y"])
        if p["is_anomaly"]:
            color = "#e53935"
            r = 5
            # draw an X marker for anomalies
            d = 4
            lines.append(
                f'<g class="anom-pt" data-label="{p["label"]}" '
                f'data-anom="{p["x"]:.3f}" data-snr="{p["y"]:.1f}">'
                f'<circle cx="{px:.1f}" cy="{py:.1f}" r="8" '
                f'fill="transparent" stroke="none"/>'
                f'<line x1="{px-d:.1f}" y1="{py-d:.1f}" '
                f'x2="{px+d:.1f}" y2="{py+d:.1f}" '
                f'stroke="{color}" stroke-width="2"/>'
                f'<line x1="{px+d:.1f}" y1="{py-d:.1f}" '
                f'x2="{px-d:.1f}" y2="{py+d:.1f}" '
                f'stroke="{color}" stroke-width="2"/>'
                f'</g>'
            )
        else:
            color = "#aaa"
            lines.append(
                f'<circle class="anom-pt" cx="{px:.1f}" cy="{py:.1f}" r="4" '
                f'fill="{color}" fill-opacity="0.6" stroke="none" '
                f'data-label="{p["label"]}" '
                f'data-anom="{p["x"]:.3f}" data-snr="{p["y"]:.1f}"/>'
            )

    # legend
    lx = margin["l"] + pw - 90
    ly = margin["t"] + 12
    lines.append(f'<circle cx="{lx}" cy="{ly}" r="4" fill="#aaa" opacity="0.6"/>')
    lines.append(f'<text x="{lx+8}" y="{ly+3}" font-size="10" fill="#666">Normal</text>')
    # X marker for anomaly legend
    lx2 = lx
    ly2 = ly + 16
    lines.append(f'<line x1="{lx2-3}" y1="{ly2-3}" x2="{lx2+3}" y2="{ly2+3}" '
                 f'stroke="#e53935" stroke-width="2"/>')
    lines.append(f'<line x1="{lx2+3}" y1="{ly2-3}" x2="{lx2-3}" y2="{ly2+3}" '
                 f'stroke="#e53935" stroke-width="2"/>')
    lines.append(f'<text x="{lx2+8}" y="{ly2+3}" font-size="10" fill="#666">Anomaly</text>')

    lines.append('</svg>')
    return "\n".join(lines)


def _anomaly_hover_js():
    """JavaScript for hover tooltips on the anomaly SVG scatter."""
    return """
<script>
(function() {
    var wrap = document.getElementById('anomaly-wrap');
    if (!wrap) return;
    var tip = document.getElementById('anomaly-tip');
    var pts = wrap.querySelectorAll('.anom-pt');
    pts.forEach(function(el) {
        el.style.cursor = 'pointer';
        el.addEventListener('mouseenter', function(e) {
            var label = el.getAttribute('data-label');
            var anom = el.getAttribute('data-anom');
            var snr = el.getAttribute('data-snr');
            tip.textContent = label + '  (anomaly=' + anom + ', SNR=' + snr + ')';
            tip.style.display = 'block';
        });
        el.addEventListener('mousemove', function(e) {
            var rect = wrap.getBoundingClientRect();
            tip.style.left = (e.clientX - rect.left + 12) + 'px';
            tip.style.top = (e.clientY - rect.top - 24) + 'px';
        });
        el.addEventListener('mouseleave', function() {
            tip.style.display = 'none';
        });
    });
})();
</script>
"""


