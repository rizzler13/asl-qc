"""
Cohort-level HTML QC report.

Generates a publication-quality group summary suitable as a data-quality
appendix in multicenter studies.  All plots are embedded as base64 PNGs
and the HTML is fully self-contained (no CDN links).
"""
import base64
import datetime
import io
import logging
import math
from pathlib import Path

import numpy as np

log = logging.getLogger(__name__)

__version__ = "0.3.0"


def write_cohort_html(cohort_results, path):
    """Write a self-contained cohort QC HTML report."""
    ts = datetime.datetime.now().strftime("%Y-%m-%d %H:%M")
    n = cohort_results.get("n_subjects", 0)
    summary = cohort_results.get("summary", {})
    subjects = cohort_results.get("subjects", [])
    metric_summary = cohort_results.get("metric_summary", {})
    if_results = cohort_results.get("isolation_forest")

    html = f"""<!DOCTYPE html>
<html lang="en">
<head>
<meta charset="UTF-8">
<meta name="viewport" content="width=device-width, initial-scale=1.0">
<title>ASL QC — Cohort Report ({n} subjects)</title>
{_css()}
</head>
<body>
<div class="container">

<h1>ASL Quality Control — Cohort Report</h1>
<p class="meta">
    ASL QC Toolbox v{__version__} &middot; {n} subjects &middot; {ts}
</p>

{_dataset_summary(summary, n)}
{_metric_summary_table(metric_summary)}
{_distribution_plots(subjects, metric_summary)}
{_correlation_matrix(subjects)}
{_anomaly_section(subjects, if_results)}
{_subject_table(subjects)}
{_methods_footnote()}

<div class="footer">
Generated by ASL QC Toolbox v{__version__} &middot; {ts}
</div>
</div>
{_sort_js()}
</body>
</html>"""

    p = Path(path)
    p.parent.mkdir(parents=True, exist_ok=True)
    with open(p, "w") as f:
        f.write(html)
    log.info("cohort html -> %s", p)
    return str(p)


