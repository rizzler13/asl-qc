def _fmt(val, fmt_str=".3f"):
    if val is None:
        return "—"
    try:
        if math.isfinite(val):
            return f"{val:{fmt_str}}"
    except (TypeError, ValueError):
        pass
    return "—"


def _is_finite(v):
    try:
        return math.isfinite(v)
    except (TypeError, ValueError):
        return False


